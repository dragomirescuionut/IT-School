package session3_javaOperators.challenges;

public class Challenge4 {
    public static void main(String[] args) {
        for(int i = 0 ; i <=10; i++){
            System.out.println("Counting up: " + i);
        }
        for(int j = 10 ; j >=1; j--){
            System.out.println("Counting down: " + j);
        }
    }
}
