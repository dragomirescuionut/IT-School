package session3_javaOperators.challenges;

public class Challenge7 {
    public static void main(String[] args) {
        int myInt = 28;
        double myDouble = 3.5;

        double sum = myInt + myDouble;
        double dif = myInt - myDouble;
        double mult = myInt * myDouble;
        double div = myInt / myDouble;

        System.out.println("Adition: " + sum);
        System.out.println("Substraction: " + dif);
        System.out.println("Multiplication: " + mult);
        System.out.println("Division: " + div);
    }
}
